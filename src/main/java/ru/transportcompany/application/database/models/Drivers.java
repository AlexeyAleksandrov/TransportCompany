package ru.transportcompany.application.database.models;

import javax.persistence.*;
import java.util.Objects;

@Entity
public class Drivers
{
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Id
    @Column(name = "id")
    private long id;
    @Basic
    @Column(name = "first_name")
    private String firstName;
    @Basic
    @Column(name = "last_name")
    private String lastName;
    @Basic
    @Column(name = "patronymic")
    private String patronymic;

    public long getId()
    {
        return id;
    }

    public void setId(long id)
    {
        this.id = id;
    }

    public String getFirstName()
    {
        return firstName;
    }

    public void setFirstName(String firstName)
    {
        this.firstName = firstName;
    }

    public String getLastName()
    {
        return lastName;
    }

    public void setLastName(String lastName)
    {
        this.lastName = lastName;
    }

    public String getPatronymic()
    {
        return patronymic;
    }

    public void setPatronymic(String patronymic)
    {
        this.patronymic = patronymic;
    }

    @Override
    public boolean equals(Object o)
    {
        if (this == o)
        {
            return true;
        }
        if (o == null || getClass() != o.getClass())
        {
            return false;
        }
        Drivers drivers = (Drivers) o;
        return id == drivers.id && Objects.equals(firstName, drivers.firstName) && Objects.equals(lastName, drivers.lastName) && Objects.equals(patronymic, drivers.patronymic);
    }

    @Override
    public int hashCode()
    {
        return Objects.hash(id, firstName, lastName, patronymic);
    }
}
